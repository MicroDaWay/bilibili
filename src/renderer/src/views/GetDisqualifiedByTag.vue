<!-- 根据标签查询取消稿件 -->
<script setup>
import { ref } from 'vue'
import SearchByTag from '../components/SearchByTag.vue'
import { format } from 'date-fns'

const itemList = ref([])
const columns = [
  { title: '标题', key: 'title' },
  { title: '投稿话题', key: 'topic', width: '20%' },
  { title: '播放量', key: 'play', width: '8%' },
  {
    title: '投稿时间',
    key: 'postTime',
    width: '20%',
    formatter: (value) => format(value, 'yyyy-MM-dd HH:mm:ss')
  }
]

// 点击搜索的处理函数
const searchHandler = async (tag) => {
  const result = await window.electronAPI.getDisqualificationByTag(tag)
  itemList.value = result

  if (result.length === 0) {
    window.electronAPI.showMessage({
      title: '根据标签查询取消稿件',
      type: 'info',
      message: '未查询到相关数据'
    })
  } else {
    window.electronAPI.showMessage({
      title: '根据标签查询取消稿件',
      type: 'info',
      message: '查询结束'
    })
  }
}
</script>

<template>
  <SearchByTag
    :title="title"
    :item-list="itemList"
    :columns="columns"
    @search-handler="searchHandler"
  ></SearchByTag>
</template>

